package Exercises.lesson4;

public class TestMathStaticMethods {
    public static void main(String[] args) {
        System.out.println("random number is  " + Math.random());
        System.out.println("square root is " + Math.sqrt(99.00));
        System.out.println("rounded random " + Math.round(Math.random() *100));
        System.out.println("absolute  " + Math.abs(9));
    }
}
